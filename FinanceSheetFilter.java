package trialProject;

public class FinanceSheetFilter {

	private String countryNames;
	
	private String regionNames;
	
	private String cropNames;
	
	private String projectFullNames;

	public String getCountryNames() {
		return countryNames;
	}

	public void setCountryNames(String countryNames) {
		this.countryNames = countryNames;
	}

	public String getRegionNames() {
		return regionNames;
	}

	public void setRegionNames(String regionNames) {
		this.regionNames = regionNames;
	}

	public String getCropNames() {
		return cropNames;
	}

	public void setCropNames(String cropNames) {
		this.cropNames = cropNames;
	}

	public String getProjectFullNames() {
		return projectFullNames;
	}

	public void setProjectFullNames(String projectFullNames) {
		this.projectFullNames = projectFullNames;
	}
	
	
	
	
}
